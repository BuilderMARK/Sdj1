public class Lektion4_01
{
  public static void main(String[] args)
  {
    int tal =0;
    String hej = "Hello";

    for (int i = 0; i < 5; i++)
    {
      tal++;
      System.out.println(tal + " " + hej);
    }
    for (int i = 5; i > 0 ; i--)
    {
      tal--;
      System.out.println(tal + " "+ hej );
    }

  }
}
